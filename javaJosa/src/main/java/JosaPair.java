public class JosaPair {

    private String josa1;
    private String josa2;

    public JosaPair(String josa1, String josa2) {
        this.josa1 = josa1;
        this.josa2 = josa2;
    }

    public String getJosa1() {
        return josa1;
    }

    public String getJosa2() {
        return josa2;
    }
}
